/**
 * gen-bytecodes.js
 * Reads compiled Hardhat artifacts from contracts/evm/artifacts and generates
 * src/config/bytecodes.ts for use by the Smart Runtime Manager DApp.
 *
 * Usage: node scripts/gen-bytecodes.js
 * (run from the web/ directory)
 */

import { readFileSync, writeFileSync, existsSync } from "fs";
import { resolve, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const artifactBase = resolve(__dirname, "../../contracts/evm/artifacts/contracts");

const artifacts = {
	SmartRuntime: "Runtime.sol/SmartRuntime.json",
	DiamondCutPallet: "pallets/DiamondCutPallet.sol/DiamondCutPallet.json",
	DiamondLoupePallet: "pallets/DiamondLoupePallet.sol/DiamondLoupePallet.json",
	OwnershipPallet: "pallets/OwnershipPallet.sol/OwnershipPallet.json",
	ProofOfExistencePallet: "pallets/ProofOfExistencePallet.sol/ProofOfExistencePallet.json",
	AccessControlPallet: "pallets/AccessControlPallet.sol/AccessControlPallet.json",
	PausablePallet: "pallets/PausablePallet.sol/PausablePallet.json",
	FungibleTokenPallet: "pallets/FungibleTokenPallet.sol/FungibleTokenPallet.json",
	NonFungibleTokenPallet: "pallets/NonFungibleTokenPallet.sol/NonFungibleTokenPallet.json",
	MultiAssetTokenPallet: "pallets/MultiAssetTokenPallet.sol/MultiAssetTokenPallet.json",
};

let out = `// Auto-generated by scripts/gen-bytecodes.js — do not edit manually
// Run: npm run codegen   (from web/ directory, requires compiled contracts)

export const bytecodes: Record<string, \`0x\${string}\`> = {\n`;

let found = 0;
for (const [name, relPath] of Object.entries(artifacts)) {
	const fullPath = resolve(artifactBase, relPath);
	if (existsSync(fullPath)) {
		const { bytecode } = JSON.parse(readFileSync(fullPath, "utf-8"));
		out += `  ${name}: "${bytecode}",\n`;
		found++;
	} else {
		out += `  // ${name}: not found — run: cd ../contracts/evm && npx hardhat compile\n`;
	}
}

out += `};\n`;

const outPath = resolve(__dirname, "../src/config/bytecodes.ts");
writeFileSync(outPath, out);
console.log(`✓ Generated bytecodes.ts with ${found}/${Object.keys(artifacts).length} contracts`);
